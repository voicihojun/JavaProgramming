package academy.learnprogramming;

public class Main {

    public static void main(String[] args) {
	    float myMinFloatNumber = Float.MIN_VALUE;
        float myMaxFloatNumber = Float.MAX_VALUE;
        System.out.println("my min float value = " + myMinFloatNumber);
        System.out.println("my max float value = " + myMaxFloatNumber);

        double myMinDoubleNumber = Double.MIN_VALUE;
        double myMaxDoubleNumber = Double.MAX_VALUE;
        System.out.println("my min double value = " + myMinDoubleNumber);
        System.out.println("my max double value = " + myMaxDoubleNumber);

        int myIntNumber = 5 / 3;
        float myFloatNumber = 5f / 3f;
        double myDoubleNumber = 5d / 3f;

        System.out.println("my int value = " + myIntNumber);
        System.out.println("my float value = " + myFloatNumber);
        System.out.println("my double value = " + myDoubleNumber);



    }
}
